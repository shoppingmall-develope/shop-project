CREATE TABLE MYSHOP_ORDERS(
    OID NUMBER CONSTRAINT PK_SHOP_ORDER_OID PRIMARY KEY,   --주문번호
    PID NUMBER NOT NULL,                 --상품번호
    ID VARCHAR2(20) NOT NULL,            --회원아이디
    STATUS number NOT NULL,        --주문상태
    ODATE DATE DEFAULT SYSDATE,                   --주문일자
    RECIPIENT_ZONECODE VARCHAR2(30) NOT NULL,     --배송지 우편번호
    RECIPIENT_ADDR VARCHAR2(50) NOT NULL,         --배송지 주소
    RECIPIENT_NAME VARCHAR2(10) NOT NULL,                --수령자 이름
    RECIPIENT_HP VARCHAR2(10) NOT NULL,                  --수령자 통신사
    RECIPIENT_PNUMBER VARCHAR2(20) NOT NULL,             --수령자 번호
    OADDR VARCHAR2(50) NOT NULL,               --배송지 주소
    QUANTITY NUMBER NOT NULL,                  --상품수량/개수
    PAYMENT VARCHAR2(10) NOT NULL,             --지불금액
    CONSTRAINT FK_SHOP_MEMBER_ID FOREIGN KEY(ID) REFERENCES MYSHOP_MEMBER(ID),
    CONSTRAINT FK_SHOP_PRODUCT_PID FOREIGN KEY(PID) REFERENCES MYSHOP_PRODUCT(PID)
);

