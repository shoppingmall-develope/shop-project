-- 카트 테이블 
CREATE TABLE MYSHOP_CART(
	BID NUMBER PRIMARY KEY ,
    AMT NUMBER NOT NULL,
    ID VARCHAR2(15) NOT NULL,
    PID NUMBER NOT NULL,       --상품코드
    BDATE DATE,
 	CONSTRAINT FK_SHOP_CART_ID FOREIGN KEY(ID) REFERENCES MYSHOP_MEMBER(ID),
    CONSTRAINT FK_SHOP_CART_PID FOREIGN KEY(PID) REFERENCES MYSHOP_PRODUCT(PID)
);

--회원테이블 
CREATE TABLE MYSHOP_MEMBER(
    ID VARCHAR2(20) CONSTRAINT PK_SHOP_MEMBER_ID PRIMARY KEY,           --고객아이디
    PASS VARCHAR2(20) NOT NULL,                  --비밀번호
    NAME VARCHAR2(10) NOT NULL,                --이름
    EMAIL VARCHAR2(30) NOT NULL,               --이메일
    ZONECODE VARCHAR2(30) NOT NULL,            --우편번호
    HP VARCHAR2(10) CONSTRAINT SHOP_MEMBER_HP_CHECK CHECK(HP IN('SK','KT','LG')),  --통신사
    PNUMBER VARCHAR2(30) NOT NULL,   --휴대폰 번호
    ADDR     VARCHAR2(50) NOT NULL,  --주소
    BIRTH     VARCHAR2(15) NOT NULL, --생년월일
    MDATE  DATE,  --가입일
    ACC number not null,
	visit date,
    GRADE VARCHAR2(8) CONSTRAINT SHOP_MEMBER_GRADE_EHECK CHECK(GRADE IN('BASIC','VIP','VVIP')), --등급
    EMAIL_CHECK CHAR(1) CONSTRAINT SHOP_MEMBER_EMAIL_CHECK CHECK(EMAIL_CHECK IN('Y','N')),  --이용약관 또는 이메일 수신여부          
    GENDER  CHAR(1) CONSTRAINT SHOP_MEMBER_GENDER_CHECK CHECK(GENDER IN('M','F'))     --성별             
);

--상품테이블 
CREATE TABLE MYSHOP_PRODUCT(
    PID NUMBER CONSTRAINT PK_SHOP_PRODUCT_PID PRIMARY KEY, --상품번호
    CATEGORY_ID NUMBER NOT NULL,       --카테고리
    PNAME VARCHAR2(100) NOT NULL,              --상품명
    BRAND VARCHAR2(40) NOT NULL,              --브랜드명
    PSIZE VARCHAR2(5) NOT NULL,                --상품 사이즈
    QUANTITY NUMBER NOT NULL,                 --수량
    PRICE NUMBER NOT NULL,                    --상품가격 
    DELIVERY_PRICE NUMBER NOT NULL,           --배송비
    REGDATE DATE DEFAULT SYSDATE,            --상품 등록일
    UPDATEDATE DATE DEFAULT SYSDATE,         --상품 수정일 
    RATING VARCHAR2(20),                      --상품 평점
    PDETAIL VARCHAR2(500),                    --상품 상세설명
    PFILE VARCHAR2(200) NOT NULL,              --상품이미지
    PSFILE VARCHAR2(200) NOT NULL,             --상품이미지
    CONSTRAINT FK_SHOP_CATEGORY_ID FOREIGN KEY(CATEGORY_ID) REFERENCES MYSHOP_CATEGORY(CATEGORY_ID)
);

-- 상품 시퀀스
CREATE SEQUENCE SEQU_SHOP_PRODUCT
    START WITH 1
    INCREMENT BY 1;

--리뷰테이블
CREATE TABLE MYSHOP_REVIEW(
    RID VARCHAR(15) CONSTRAINT PK_SHOP_REVIEW_RID PRIMARY KEY,
    PID NUMBER NOT NULL,                    --리뷰한 상품 아이디
    OID NUMBER NOT NULL,         --주문 아이디
    RWRITER VARCHAR2(15) NOT NULL,               --리뷰 작성자
    SCORE NUMBER(1),                        --사용자 평점
    RCONTENT VARCHAR2(300),                 --리뷰내용
    RFILE VARCHAR2(200),            
    RSFILE VARCHAR2(200),
    RECOM NUMBER(5) NOT NULL,               --추천 수
    RDATE DATE,                              --작성일
    CONSTRAINT FK_SHOP_review_OID FOREIGN KEY(OID) REFERENCES MYSHOP_ORDERS(OID),
    CONSTRAINT FK_SHOP_review_PID FOREIGN KEY(PID) REFERENCES MYSHOP_PRODUCT(PID)
    );

